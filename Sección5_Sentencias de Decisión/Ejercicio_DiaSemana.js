//Ejercicio para determinar el día de la semana
let diaSemama=7;

if(diaSemama===1){
    console.log("Lunes");
}
else if(diaSemama===2){
    console.log("Martes");
}
else if(diaSemama===3){
    console.log("Miércoles");
}
else if(diaSemama===4){
    console.log("Jueves");  
}
else if(diaSemama===5){
    console.log("Viernes");             
}
else if(diaSemama===6){
    console.log("Sábado");
}
else if(diaSemama===7){
    console.log("Domingo");
}
else{
    console.log("Número de día inválido");
}
