//Operadores de Comparación o Relacionales
let a=5;
let b='5';
console.log(a);
console.log(b);

//Operadores igualdad ==
//solo compara valores, no el tipo, y hace una conversion si es necesario
console.log("a==b ->",a==b);

//String interpolation
console.log(`${a} == ${b} -> ${a==b}`)

//Operador igualdad estricta o exacto
//compara valor y tipo de dato
console.log("a===b ->",a===b);

//String interpolation
console.log(`${a} === ${b} -> ${a===b}`)

//Operadores de distinción
//compara valor y convierte el tipo de dato si es necesario
console.log(`${a} != ${b} -> ${a!=b}`)

//Operador distinto exacto
//compara el valor y el tipo de dato
console.log(`${a} !== ${b} -> ${a!==b}`)

